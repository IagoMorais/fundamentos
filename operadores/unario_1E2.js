

/*Operador de negação unária (-): Este operador converte um valor para um
 número e, em seguida, inverte o sinal desse número.*/

 var num = 42;
 var negativo = -num;
 console.log(negativo); // -42
 
 //============================================================
 // Operador de incremento unário (++): Este operador incrementa o 
 //valor de uma variável em uma unidade.
 
 var contador = 0;
 contador++;
 console.log(contador); // 1
 
 //=================================================================================
 //  Operador de decremento unário (--): Este operador decrementa 
 //o valor de uma variável em uma unidade.
 
 var contador = 10;
 contador--;
 console.log(contador); // 9
 
 /*=================================================================================
 Operador de tipo de dado unário (typeof): Este operador retorna 
 uma string que representa o tipo de dado de uma variável ou expressão.*/
 
 var num = 42;
 var str = "hello";
 var bool = true;
 
 console.log(typeof num); // "number"
 console.log(typeof str); // "string"
 console.log(typeof bool); // "boolean"
 
 /*===================================================================================
  Operador de inversão lógica unária (!): Este operador 
 inverte o valor de um booleano, transformando true em false e false em true.*/
 
 var condicao = true;
 var resultado = !condicao;
 console.log(resultado); // false
 
 
